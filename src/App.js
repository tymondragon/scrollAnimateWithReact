    // Draw in reverse
    this.setState({pathClass: {...this.state.pathClass, strokeDashoffset: `${this.state.pathDistance-drawLength}`}})
    // path.style.strokeDashoffset = pathLength - drawLength
    if (scrollPercentage >= 0.99) {
       // path.style.strokeDasharray = "none"
       this.setState({ pathClass: {...this.state.pathClass, strokeDasharray: "none"}})
     } else {
       this.setState({ pathClass: {...this.state.pathClass, strokeDasharray: `${this.state.pathDistance} ${this.state.pathDistance}`}})
       // path.style.strokeDasharray = pathLength + ' ' + pathLength
     }
